    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String operacao = "Incluir";
        JOptionPane.showMessageDialog(null, "variável global: "+operacaoAtivaGlobal);

        if (operacaoAtivaGlobal.equals(operacao)) {
            JOptionPane.showMessageDialog(null, "Inclusão sendo executada com sucesso!");
            
            usuarios dados_usuarios = new usuarios();

            dados_usuarios.setId(jTextField1.getText());
            dados_usuarios.setSenha(jTextField2.getText());
            dados_usuarios.setNumAge(jTextField3.getText());
            dados_usuarios.setNumCc(jTextField4.getText());

            connectDAO objcon = new connectDAO();

            objcon.insereRegistroJFBD("USUARIOS", dados_usuarios.dadosSQLValues());

            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        }
        
        if (operacaoAtivaGlobal.equals(operacao)) {            
            usuarios dados_usuarios = new usuarios();

            dados_usuarios.setId(jTextField1.getText());
            dados_usuarios.setSenha(jTextField2.getText());
            dados_usuarios.setNumAge(jTextField3.getText());
            dados_usuarios.setNumCc(jTextField4.getText());

            connectDAO objcon = new connectDAO();

            objcon.alterarRegistroJFBD("USUARIOS", dados_usuarios.alterarRegistroJFBD(), "NUM_AGE=" + jTextField1.getText());

            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        }
        
        operacao = "Alterar";
 
        if (operacaoAtivaGlobal.equals(operacao)) {
            JOptionPane.showMessageDialog(null, "variável local: "+operacao);
            JOptionPane.showMessageDialog(null, "variável global: "+operacaoAtivaGlobal);
            connectDAO objcon = new connectDAO();
            usuarios_tela = objcon.pesquisaClienteJFBD("USUARIOS", "ID = '" + jTextField1.getText() + "'");
            jTextField2.setText(usuarios_tela.getSenha());
            jTextField3.setText(usuarios_tela.getNumAge());
            jTextField4.setText(usuarios_tela.getNumCc());
            JOptionPane.showMessageDialog(null, "pesquisou dados ");
            jLabel1.setVisible(true);                       // deixando somente o label e o TextField do Id para pesquisar como true (visível).
            jLabel2.setVisible(true);
            jLabel3.setVisible(true);
            jLabel4.setVisible(true);
            jTextField1.setVisible(true);
            jTextField2.setVisible(true);
            jTextField3.setVisible(true);
            jTextField4.setVisible(true);
            jButton1.setText("Alterar");
            operacaoAtivaGlobal = "Alteração";
            JOptionPane.showMessageDialog(null, "passou pela liberação dos jtextfields e ficou com: "+operacaoAtivaGlobal);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

